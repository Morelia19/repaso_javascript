//Javascript soporta varios tipos de valres
var a = 1; //número (int)
var b = 0.01 //decimales (float, double)
var c = "hola"; //cadena de caracteres (String)
var d = true; //booleano verdadero (boolean)
var e = false; //booleano falso (boolean)
